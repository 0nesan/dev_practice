#include <stdio.h>

// 메크로 상수 예제
#define PI 3.14159

/** 메크로 상수란 ?
* 메크로 상수는 프로그램에서 자주 사용되는 값을 정의할 때 사용합니다.
* 메크로 상수는 컴파일 타임에 값이 결정되며, 코드의 가독성을 높이고 유지보수를 용이하게 합니다.
* 예시: 원의 넓이를 계산하는 함수
*/

/** 메크로 상수가 전역 변수와 다른점은 ?
* 메크로 상수는 전역 변수와 달리 메모리를 차지하지 않으며, 코드 내에서 직접적으로 값으로 대체됩니다.
* 메크로 상수는 프로그램의 성능에 영향을 주지 않습니다.
*/

// 전역 변수 예시
int global_var = 10;
/** 전역 변수는 프로그램 전체에서 접근할 수 있는 변수로, 메모리를 차지합니다.
* 전역 변수는 프로그램의 상태를 유지하는 데 사용되지만, 코드의 가독성을 떨어뜨릴 수 있습니다.
* 전역 변수는 프로그램의 모든 함수에서 접근할 수 있으며, 값이 변경될 수 있습니다.
*/

// 함수 선언 예시
int add(int a, int b);

/** 메인 함수와 메인 파일은 꼭 필요할까?
* 메인 함수는 C 프로그램의 시작점으로, 모든 C 프로그램은 반드시 main 함수를 포함해야 합니다.
* 메인 파일은 프로그램의 진입점으로, 컴파일러가 프로그램을 실행할 때 가장 먼저 실행되는 파일입니다.
* 따라서, 메인 함수와 메인 파일은 C 프로그램에서 필수적입니다.
*/
int main(void) {
    // 지역 변수 선언
    const int local_var = 10;

    // 함수 호출
    const int result = add(local_var, global_var);

    printf("Result: %d\n", result);

    return 0;
}

// 함수 정의 예시
int add(const int a, const int b) {
    return a + b;
}

// 리터럴 상수
int age = 25;
float height = 175.5f;
char initial = 'A';
/** 왜 함수 내의 변수에만 const로 선언해야하는가?
* 함수 내의 변수에 const로 선언하는 이유는 해당 변수가 함수 내에서 변경되지 않음을 보장하기 위해서입니다.
* const로 선언된 변수는 읽기 전용으로, 값이 변경되지 않도록 보호합니다.
* 이는 코드의 안정성을 높이고, 의도치 않은 값 변경을 방지하는 데 도움이 됩니다.
*/

/** 그럼 함수 바깥에 있는 변수는 const로 선언할 필요가 없는가?
* 함수 바깥에 있는 변수는 전역 변수로, 프로그램 전체에서 접근할 수 있습니다.
* 전역 변수는 프로그램의 상태를 유지하는 데 사용되지만, 값이 변경될 수 있습니다.
* 따라서, 전역 변수를 const로 선언하는 것은 일반적으로 권장되지 않습니다.
*/